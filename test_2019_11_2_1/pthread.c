#include<stdio.h>
#include <unistd.h>
#include <stdlib.h>
#include <pthread.h>
#include <string.h>
#include <time.h>
void* rout(void* arg)
{
    while(1)
    {
        printf("I am thread 1:%s\n",(char*)arg);
        sleep(1);
        break;
    }
//    return (void*)11;
    pthread_exit((void*)3);//线程终止
}
int main()
{
    pthread_t tid;
    pthread_create(&tid,NULL,rout,(void*)"thread 1");
    printf("I am main thread:%p\n",tid);
    void* ret;
    pthread_join(tid,&ret);//线程等待，以阻塞方式等待
    printf("ret:%d\n",(int)ret);
    return 0;
}
